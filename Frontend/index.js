const callerAddress = "";
const oracleAddress = "";
const providerAddress = "";

let contractCaller;
let contractOracle;

async function connectWallet() {
    try {
        const provider = new ethers.providers.JsonRpcProvider("");

        return provider;
    } catch (error) {
        console.error("Error when connecting provider:", error);
    }
}

function connectContract(address, abi, signer) {
    return new ethers.Contract(address, abi, signer);
}

async function addProvider() {
    await contractCaller.setOracleAddress(oracleAddress);
    await contractOracle.addProvider(providerAddress);
}

async function getWinner() {
    await contractCaller.getWinnerFight();
}
async function main() {
    const provider = await connectWallet();
    const signer = provider.getSigner();

    contractCaller = connectContract(callerAddress, abiCaller.abi, signer);
    contractOracle = connectContract(oracleAddress, abiOracle.abi, signer);

    contractCaller.on("WinnerFightReceived", async (result) => { console.log("Result of WinnerFightReceived", result.toString()) });
    contractCaller.on("WinnerFightRequested", async (result) => { console.log("Result of WinnerFightRequested", result.toString()) });
}

main();